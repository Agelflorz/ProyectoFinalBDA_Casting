    private void tbl_perfilMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_perfilMouseClicked
        DefaultTableModel model = (DefaultTableModel) tbl_perfil.getModel();
        int Codigo = Integer.parseInt(model.getValueAt(tbl_perfil.getSelectedRow(), 0).toString());
        String Estado = model.getValueAt(tbl_perfil.getSelectedRow(), 1).toString();
        String Sexo = model.getValueAt(tbl_perfil.getSelectedRow(), 2).toString();
        String Rango = model.getValueAt(tbl_perfil.getSelectedRow(), 3).toString();
        int Edad = Integer.parseInt(model.getValueAt(tbl_perfil.getSelectedRow(), 4).toString());
        String Cabello = model.getValueAt(tbl_perfil.getSelectedRow(), 5).toString();
        String Ojos = model.getValueAt(tbl_perfil.getSelectedRow(), 6).toString();
        String Especialidad = model.getValueAt(tbl_perfil.getSelectedRow(), 7).toString();
        String Cliente = model.getValueAt(tbl_perfil.getSelectedRow(), 8).toString();
        String Tipo = model.getValueAt(tbl_perfil.getSelectedRow(), 9).toString();
        String Esperiencia = model.getValueAt(tbl_perfil.getSelectedRow(), 10).toString();

        txtConidgoCasting.setText(Codigo + "");
        txtEstado.setText(Estado);
        txtSexo.setText(Sexo);
        txtRangoAltura.setText(Rango);
        txtEdad.setText(Edad + "");
        txtColorCabello.setText(Cabello);
        txtColorOjos.setText(Ojos);
        cbm_especialidad.setSelectedItem(Especialidad);
        cbmCliente.setSelectedItem(Cliente);
        cbm_Tipo.setSelectedItem(Tipo);
        cbm_Experiencia.setSelectedItem(Esperiencia);
    }//GEN-LAST:event_tbl_perfilMouseClicked

