    private void ObtenerNombreCliente() {
        try {
            this.ClienteControl.crearConexion();
            DAO_Cliente CbmCliente = new DAO_Cliente();
            System.out.println(CbmCliente.BuscarCliente());
            List<Cliente> ListaCliente = CbmCliente.BuscarCliente();
            for (Cliente f : ListaCliente) {
                cbmCliente.addItem(f);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }
