    public void Guardar() {
        try {
            int Codigo = Integer.parseInt(this.txtConidgoCasting.getText());
            String Estado = this.txtEstado.getText();
            String Sexo = this.txtSexo.getText();
            double Rango = Double.parseDouble(this.txtRangoAltura.getText());
            int Edad = Integer.parseInt(this.txtEdad.getText());
            String Cabello = this.txtColorCabello.getText();
            String Ojos = this.txtColorOjos.getText();
            String Especialidad = cbm_especialidad.getSelectedItem().toString();
            String Cliente = cbmCliente.getSelectedItem().toString();
            String Tipo = cbm_Tipo.getSelectedItem().toString();
            String Esperiencia = cbm_Experiencia.getSelectedItem().toString();
            PerfilCasting PerfilNew = new PerfilCasting(Codigo, Estado, Sexo, Rango, Edad, Cabello, Ojos, Especialidad, Cliente, Tipo, Esperiencia);
            PerfilControl.agregar(PerfilNew);
            JOptionPane.showMessageDialog(this, "El Perfil se agrego con exito.,",
                    "Notificaci贸n.", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            System.out.println(e);
        }

    }

    public void Eliminar() {
        int indice = this.tbl_perfil.getSelectedRow();
        if (indice != -1) {
            DefaultTableModel modeloTabla = (DefaultTableModel) this.tbl_perfil.getModel();
            int idperfil = (int) modeloTabla.getValueAt(indice, 0);
            PerfilControl.eliminar(idperfil);
            JOptionPane.showMessageDialog(this, "El Casting se elimin贸 con exito.,",
                    "Notificaci贸n.", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Se debe seleccionar un elemento de la tabla,"
                    + "por favor, seleccione una opci贸n valida.",
                    "Error.", JOptionPane.ERROR_MESSAGE);
        }

    }

    private void centrarVentana() {
        Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
        Dimension frameSize = getSize();

        if (frameSize.height > screenSize.height) {
            frameSize.height = screenSize.height;
        }
        if (frameSize.width > screenSize.width) {
            frameSize.width = screenSize.width;
        }

        setLocation((screenSize.width - frameSize.width) / 2,
                (screenSize.height - frameSize.height) / 2);
    }

